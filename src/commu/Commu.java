package commu;

public class Commu {
	private int commuID;
	private String commuTitle;
	private String userID;
	private String commuDate;
	private String commuContent;
	private int commuAvailable;
	
	public int getCommuID() {
		return commuID;
	}
	public void setCommuID(int commuID) {
		this.commuID = commuID;
	}
	public String getCommuTitle() {
		return commuTitle;
	}
	public void setCommuTitle(String commuTitle) {
		this.commuTitle = commuTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getCommuDate() {
		return commuDate;
	}
	public void setCommuDate(String commuDate) {
		this.commuDate = commuDate;
	}
	public String getCommuContent() {
		return commuContent;
	}
	public void setCommuContent(String commuContent) {
		this.commuContent = commuContent;
	}
	public int getCommuAvailable() {
		return commuAvailable;
	}
	public void setCommuAvailable(int commuAvailable) {
		this.commuAvailable = commuAvailable;
	}
}
